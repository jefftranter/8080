;;      8250 UART CONTROL AND BIT DEFINITIONS.

SC.ACE  EQU     350Q            ; SYSTEM CONSOLE PORT IF 8250 ACE
AC.DLY  EQU     110             ; 220 MIL. SEC. DELAY FOR 8250

UR.RBR  EQU     0               ; RECEIVE BUFFER REGISTER (READ ONLY)

UR.THR  EQU     0               ; TRANSMITTER HOLDING REGISTER (WRITE ONLY)

UR.DLL  EQU     0               ; DIVISOR LATCH (LEAST SIGNIFICANT)

UR.DLM  EQU     1               ; DIVISOR LATCH (MOST SIGNIFICANT)

UR.IER  EQU     1               ; INTERRUPT ENABLE REGISTER
UC.EDA  EQU     00000001B       ;  ENABLE RECEIVED DATA AVALABLE INTERRUPT
UC.TRE  EQU     00000010B       ;  ENABLE TRANSMIT HOLD REGISTER EMPTY INTERRUPT
UC.RSI  EQU     00000100B       ;  ENABLE RECEIVE STATUS INTERRUPT
UC.MSI  EQU     00001000B       ;  ENABLE MODEM STATUS INTERRUPT

UR.IIR  EQU     2               ; INTERRUPT IDENITIFICATION REGISTER
UC.IIP  EQU     00000001B       ;  INVERTED INTERRUPT PENDING (0 MEANS PENDING)
UC.IID  EQU     00000110B       ;  INTERRUPT ID

UR.LCR  EQU     3               ; LINE CONTROL REGISTER
UC.5BW  EQU     00000000B       ;  5 BIT WORDS
UC.6BW  EQU     00000001B       ;  6 BIT WORDS
UC.7BW  EQU     00000010B       ;  7 BIT WORDS
UC.8BW  EQU     00000011B       ;  8 BIT WORDS
UC.2SB  EQU     00000100B       ;  TWO STOP BITS SELECTED
UC.PEN  EQU     00001000B       ;  PARITY COMPUTATION ENABLED
UC.EPS  EQU     00010000B       ;  EVEN PARITY SELECTE
UC.SKP  EQU     00100000B       ;  STICK PARITY
UC.SB   EQU     01000000B       ;  SET BREAK
UC.DLA  EQU     10000000B       ;  DIVIDER LATCH ACCESS

UR.MCR  EQU     4               ; MODEM CONTROL REGISTER
UC.DTR  EQU     00000001B       ;  DATA TERMINAL READY
UC.RTS  EQU     00000010B       ;  REQUEST TO SEN
UC.OU1  EQU     00000100B       ;  OUT 1
UC.OU2  EQU     00001000B       ;  OUT 2
UC.LOO  EQU     00010000B       ;  LOOP

UR.LSR  EQU     5               ; LINE STATUS REGISTER
UC.DR   EQU     00000001B       ;  DATA READY
UC.OR   EQU     00000010B       ;  OVERRUN
UC.PE   EQU     00000100B       ;  PARITY ERROR
UC.FE   EQU     00001000B       ;  FRAMING ERROR
UC.BI   EQU     00010000B       ;  BREAK INTERRUPT
UC.THE  EQU     00100000B       ;  TRANSMITTER HOLDING REGISTER EMPTY
UC.TSE  EQU     01000000B       ;  TRANSMITTER SHIFT REGISTER EMPTY

UR.MSR  EQU     6               ; MODEM STATUS REGISTER
UC.DCS  EQU     00000001B       ;  DELTA CLEAR TO SEND
UC.DDR  EQU     00000010B       ;  DELTA DATA SET READY
UC.TER  EQU     00000100B       ;  TRAILING EDGE OF RING
UC.DRL  EQU     00001000B       ;  DELTA RECEIVE LINE SIGNAL DETECT
UC.CTS  EQU     00010000B       ;  CLEAR TO SEND
UC.DSR  EQU     00100000B       ;  DATA SET READY
UC.RI   EQU     01000000B       ;  RING INDICATOR
UC.RLS  EQU     10000000B       ;  RECEIVED LOINE SIGNAL DETECT
